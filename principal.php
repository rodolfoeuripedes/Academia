                    <h2 align=center><font face="MS PMincho" color="blue">O Que é Bodybuilding Exatamente?</font></h2>
                    
                    <p align='justify'>
                        Quando nascemos, nossa genética vai determinar todas as nossas características físicas.
                        Já temos uma programação para altura, cor de pele e tudo mais, no entanto, algumas coisas
                        poderão ser modificadas, independente do que determina nosso DNA.
                        <br><br>
                        Uma forma de modificarmos nosso corpo é através do bodybuilding. Obviamente, há limites,
                        mas grandes transformações são possíveis, desde que acompanhadas de muito esforço e
                        determinação.
                        <br><br>
                        Então, o que é bodybuilding exatamente? A resposta para essa pergunta é bem
                        literal.
                        <br><br>
                        Bodybuilding é a “construção” de um novo corpo, uma nova forma física, através de
                        muito exercício e dieta apropriada.
                        <br><br>
                        Toda mudança externa depende de uma mudança interna, de
                        um comprometimento, que só acontece quando há vontade real. Atualmente, a tecnologia, tanto
                        na área de aparelhos de ginástica quanto no campo nutricional, implementou grandes avanços
                        nesse segmento.
                        <br><br>
                        Agora que já sabemos o que é bodybuilding, vamos esclarecer um pouco mais essa prática também
                        conhecida por fisiculturismo.
                        <br><br>

                    <h2 align=center><font face="MS PMincho" color="blue">Como se Iniciar no Bodybuilding?</font></h2>

                    <p align='justify'>
                    Antes de mergulhar de cabeça no bodybuilding, seria bastante interessante buscar apoio em alguns
                    profissionais. São eles:
                    <br>
                    <ul>
                        <li><b>Médico:</b> Nenhuma atividade física deve ser iniciada sem antes uma consulta ao médico, para avaliação,
                            e se necessário, alguns exames.</li>

                        <li><b>Nutricionista:</b> Um nutricionista também vai ajudar na definição de um cardápio personalizado, com as
                            porções equilibradas para o desempenho das atividades físicas.</li>

                        <li><b>Profissional qualificado:</b> Um personal trainer pode não ser a pessoa indicada para esse tipo de
                            treinamento. Seria muito indicado buscar alguém com qualificação específica nessa área. Alguém com
                            experiência para desenvolver um programa de exercícios, respeitando as limitações iniciais, e avançando
                            passo-a-passo, conforme os progressos do aluno. Não adianta somente ficar levantando pesos e esperando
                            os bíceps aumentarem de volume. Muitas vezes, outros grupos musculares precisam ser trabalhados, em
                            conjunto, para se atingir essa meta.</li>
                    </ul>
                    <br>

                    <h2 align=center><font face="MS PMincho" color="blue">Mudanças Iniciais</font></h2>

                    O bodybuilding só trará resultados se o praticante estiver focado nesses três itens:
                    <ul>
                        <li><b>Treino:</b> Os exercícios para a construção de um corpo não se limitam à malhação pesada, mas, obviamente,
                            a musculação é o carro chefe dos praticantes.<br>
                            Porém, o treino aeróbico é necessário para o fortalecimento cardíaco, enquanto a malhação promove uma
                            hipertrofia de grupos musculares, que se caracteriza pelo aumento do volume desses músculos.</li>

                        <li><b>Dieta:</b> A dieta para o praticante do bodybuilding, além de balanceada, deve ser muito pobre em gordura,
                            uma vez que a ideia é criar massa magra; porém, é fundamental que seja rica em nutrientes e vitaminas. Isso
                            significa 6 a 7 refeições diárias, com muita proteína e pouca gordura. Para pessoas comuns, a dieta de
                            bodybuilder parecerá um exagero, mas é preciso levar em consideração a exacerbada atividade física e muscular,
                            o que justifica esses valores hipercalóricos. É preciso que o corpo esteja preparado para a carga de exercícios
                            que irá enfrentar ao longo do dia.</li>

                        <li><b>Estilo de vida:</b> Uma vez tomada a decisão de praticar bodybuilding, é preciso ter em mente que visitas
                            esporádicas à academia não trarão resultados. É preciso ter disciplina. A atividade física deve ser alternada
                            com descanso, portanto, dormir boas horas de sono promoverão recuperação da musculatura para o treino do dia
                            seguinte. É necessário cortar cigarro e álcool.</li>
                    </ul>
                    <br>

                    <h2 align=center><font face="MS PMincho" color="blue">Bodybuilding x Anabolizantes</font></h2>
                    
                    <p align='justify'>
                    Apesar de estarem cientes sobre o que significa bodybuilding, nem todos têm o mesmo comprometimento diante dos
                    resultados esperados.
                    <br><br>
                    Muitos não terão a disciplina necessária, e preferirão queimar etapas durante o processo de desenvolvimento
                    muscular.
                    <br><br>
                    Uma forma de acelerar esse processo é através do uso dos esteróides anabolizantes, popularmente conhecidos por
                    “bomba”. De fato, os efeitos dessas substâncias no organismo são tão devastadores quanto uma bomba mesmo.
                    <br><br>
                    Nas academias é bastante comum a oferta desses produtos entre os praticantes, como também não fica difícil
                    perceber quem está fazendo uso deles. A massa muscular nessas pessoas aumenta muito mais rapidamente.
                    <br><br>
                    Há várias formas de anabolizantes disponíveis: em ampolas para aplicação intramuscular, por via oral, adesivos,
                    pomadas e cremes. Todos danosos e proibidos, no entanto, facilmente conseguidos no mercado paralelo.
                    <br><br>
                    Esse tipo de drogas tem uma indicação médica real no tratamento de algumas patologias: quando alguns homens não
                    produzem testosterona ou apresentam baixos índices desse hormônio, alguns portadores de HIV, alguns casos de
                    câncer, anorexia e patologias associadas a déficits de testosterona. Porém, as quantidades prescritas são mínimas,
                    controladas, e para um fim especifico que não é seguramente o bodybuilding.
                    <br><br>
                    Efeitos dos anabolizantes, utilizados sem controle, nem orientação:
                    <ul>
                        <li>Provocam sobrecarga hepática, que vai desde uma infecção até um câncer de fígado;</li>
                        <li>Causam arritmias;</li>
                        <li>Aumentam o colesterol ruim;</li>
                        <li>Hepatite medicamentosa;</li>
                        <li>Surgimento de acnes;</li>
                        <li>Queda de cabelos;</li>
                        <li>Nos homens podem provocar o aumento das mamas e a redução dos testículos;</li>
                        <li>Entre as mulheres, o crescimento de pelos, alterações menstruais, virilização.</li>
                    </ul>
                    <br>

                    <h2 align=center><font face="MS PMincho" color="blue">Substâncias Banidas pelos Bodybuilders</font></h2>
                    
                    <p align='justify'>
                    As substâncias abaixo foram banidas pelo WNBF (World Natural Bodybuilding Federation) e pelo INBF (International
                    Natural Bodybuilding Federation). Seu uso, quando descoberto, provocará a suspensão dos praticantes de
                    bodybuilding, de qualquer uma dessas federações, por sete anos.
                    <ul>
                        <li>Esteróides anabolizantes;</li>
                        <li>Hormônios do crescimento;</li>
                        <li>Hormônios, pró-hormônios e precursores;</li>
                        <li>Efedrina;</li>
                        <li>Anfetaminas;</li>
                        <li>Implantes musculares;</li>
                        <li>Estimulantes;</li>
                        <li>Suplementos de origem suspeita ou não comprovada.</li>
                    </ul>
                    <br>

                    <h2 align=center><font face="MS PMincho" color="blue">Considerações Finais</font></h2>
                    
                    <p align='justify'>
                    O que é bodybuilding?
                    <br><br>
                    É um estilo de vida voltado à construção de um novo corpo. Não se trata de um hobby, algo que se faz para passar
                    o tempo. É dedicação, comprometimento, abrir mão de várias coisas em função de outras, treinamentos pesados,
                    exaustão.
                    <br><br>
                    Entre os praticantes, uma frase é rotina: “No pain, no gain”.
                    <br><br>
                    Bodybuiding não se pratica somente na academia, mas principalmente fora dela, seguindo a dieta, cortando
                    hábitos não saudáveis, respeitando períodos de descanso. Portanto, é algo que se faz 24 horas por dia, 7
                    dias por semana, ininterruptamente. Essa é a única forma de se tornar um bodybuilder.
